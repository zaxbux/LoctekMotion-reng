#include "display.h"

/**
 * - Combine values to indicate multiple buttons at the same time.
 * - Send `None` to get control box to send display bytes.
 */
enum ButtonState
{
	/** Button press */
	None = 0x0000,
	/** `▲` Up */
	Raise = 0x0100,
	/** `▼` Down */
	Lower = 0x0200,
	/** `1` Preset 1 */
	Preset1 = 0x0400,
	/** `2` Preset 2*/
	Preset2 = 0x0800,
	/** `3` Preset 3*/
	Preset3 = 0x1000,
	/** `M` Preset save */
	Save = 0x2000,
	/** `A` Reminder */
	Remind = 0x4000,
};

